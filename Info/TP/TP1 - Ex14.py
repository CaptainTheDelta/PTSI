# Consigne :
print("""\nDans la console tapez les instructions qui affichent :

"J'aime beaucoup Python.
        Même si je ne comprends pas tout."
\n\n""")
# L'utilisation des """ """ permet de :
#       - pouvoir écrire directement sur plusieurs lignes;
#       - lorsqu'on effectue un retour à la ligne, pas besoin de mettre \n (!!!)

# Exercice :
print("J'aime beaucoup Python.\n\tMême si je ne comprends pas tout.")
# \n <-> retour à la ligne
# \t <-> tabulation

# Conclusion :
print("\n\\n permet correspond à un retour à la ligne, \\t correspond à une "+
    "tabulation.")